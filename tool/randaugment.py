def Posterize(img, v, max_v, bias=0):
    v = _int_parameter(v, max_v) + bias
    return PIL.ImageOps.posterize(img, v)


def Rotate(img, v, max_v, bias=0):
    v = _int_parameter(v, max_v) + bias
    if random.random() < 0.5:
        v = -v
    return img.rotate(v)


def MultiRotate(img, v, max_v, bias=0):
    v = _int_parameter(v, max_v) + bias
    if random.random() < 0.5:
        v = -v

    list = []
    for i in range(len(img)):
        if i == 0:
            fill_method = PIL.Image.BILINEAR
            fill_color = (0, 0, 0)
        else:
            fill_method = PIL.Image.NEAREST
            fill_color = 255
        list.append(img[i].rotate(v, fill_method, fillcolor=fill_color))
    return list


